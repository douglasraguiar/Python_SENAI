


num1 = float(input("Digite o primeiro número: "))
num2 = float(input("Digite o segundo número: "))

soma = (num1 + num2)
print("Resultado da soma de: ", num1, "e", num2, "é", soma)

subtracao = (num1 - num2)
print("Resultado da subtração de: ", num1, "e", num2, "é", subtracao)

divisao = (num1 // num2)
print("Resultado da divisão de: ", num1, "e", num2, "é", divisao)

multiplicacao = (num1 * num2)
print("Resultado da multiplicação de: ", num1, "e", num2, "é", multiplicacao)